package fingerprint

import (
	"strings"
	"testing"
)

func TestSummarizeRDPResponse(t *testing.T) {
	t.Parallel()
	// TPKT length 0x20 = 32 bytes total; pad so RDP_NEG_RSP at offset 19 fits under i+8 < len(b).
	b := []byte{
		0x03, 0x00, 0x00, 0x20,
		0x0e, 0xd0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x02, 0x00, 0x08, 0x00, 0x03, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
	}
	s := summarizeRDPResponse(b)
	if s == "" {
		t.Fatal("expected summary")
	}
	if !strings.Contains(s, "RDP") || !strings.Contains(s, "SSL") {
		t.Fatalf("unexpected summary: %q", s)
	}
}
